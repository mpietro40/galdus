<?php
 $a = 1;

 // Operatori di pre-incremento

echo "<br>La variabile vale: ".++$a;
 echo " e dopo vale: ".$a;

 // Operatori di post-incremento

 echo "<br>La variabile vale: ".$a++;
 echo " e dopo vale: ".$a;

 // Operatori di pre-decremento

 echo "<br>La variabile vale: ".--$a;
 echo " e dopo vale: ".$a;

 // Operatori di post-decremento

 echo "<br> La variabile vale: ".$a--;
 echo " e dopo vale: ".$a;

 ?>
